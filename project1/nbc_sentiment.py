def load_stopwords(filepath="stopwords.txt"):
    with open(filepath, 'r', encoding='utf-8') as f:
        return f.read().splitlines()

def convert_text(text):
  lowercase_text = text.lower()
  special_char = r'[^a-z0-9\s]'
  result = re.sub(special_char, '', lowercase_text)
  return result

def handle_negation(tokens):
  negate_words = ["not", "never"]
  is_negated = False
  result = []
  for token in tokens:
    if is_negated:
      result.append(token + "_NEG")
      is_negated = False
    else:
      result.append(token)

    if token in negate_words:
      is_negated = True
  return result

def preprocess(text, stopwords):
    text = convert_text(text)
    tokens = text.split()
    tokens = [token for token in tokens if token not in stopwords]
    tokens = handle_negation(tokens)
    return tokens

def ngrams_generator (token, num):
  ngrams = []
  token_length = len(token)
  bounds = token_length - num + 1
  for i in range(bounds):
    ngrams.append(" ".join(token[i:i+num]))
  return ngrams

