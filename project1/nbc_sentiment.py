def extract_features(text, stopwords, top_unigrams, top_bigrams, mode="both"):
    tokens = preprocess(text, stopwords)
    features = set()

    if "unigram" in mode or mode == "both":
        features.update(token for token in tokens if token in top_unigrams)

    if "bigram" in mode or mode == "both":
        bigrams = ngrams_generator(tokens, 2)
        features.update(bigram for bigram in bigrams if bigram in top_bigrams)

    return features

# naive bayesian
def naive_bayes(X, y, vocab):
    class_counts = {0: 0, 1: 0}
    word_counts = {0: {}, 1: {}}

    for features, label in zip(X, y):
        class_counts[label] += 1
        for word in features:
            word_counts[label][word] = word_counts[label].get(word, 0) + 1
    return class_counts, word_counts

def log_probability_calc(features, vocab, class_count, word_counts_c, total_docs):
    log_prob = math.log(class_count / total_docs)
    total_words = sum(word_counts_c.values())
    V = len(vocab)

    for word in vocab:
        count = word_counts_c.get(word, 0)
        prob = (count + 1) / (total_words + V)
        if word in features:
            log_prob += math.log(prob)
        else:
            log_prob += math.log(1 - prob)

    return log_prob

def predict(features, vocab, class_counts, word_counts):
    total_docs = sum(class_counts.values())
    log_probs = {}

    for c in [0, 1]:
        log_prob = log_probability_calc(features, vocab, class_counts[c], word_counts[c], total_docs)
        log_probs[c] = log_prob

    return 1 if log_probs[1] > log_probs[0] else 0

def evaluate(y_true, y_pred):
    tp = 0
    tn = 0
    fp = 0
    fn = 0

    for yt, yp in zip(y_true, y_pred):
        if yt == 1 and yp == 1:
            tp += 1
        elif yt == 0 and yp == 0:
            tn += 1
        elif yt == 0 and yp == 1:
            fp += 1
        elif yt == 1 and yp == 0:
            fn += 1

    precision = tp / (tp + fp) if (tp + fp) else 0
    recall = tp / (tp + fn) if (tp + fn) else 0
    f1 = 2 * precision * recall / (precision + recall) if (precision + recall) else 0
    return precision, recall, f1

